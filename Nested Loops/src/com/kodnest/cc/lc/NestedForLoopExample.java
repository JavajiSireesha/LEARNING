package com.kodnest.cc.lc;

public class NestedForLoopExample {

	public static void main(String[] args) {
		for(int i=1;i<=5;i++)
		{
			
			//nested for loop
			for(int j=1;j<=5;j++)
			{
				System.out.print("Chinni ");
			}
			System.out.println();
		}
		System.out.println("Bujji");	
			

	}

}
