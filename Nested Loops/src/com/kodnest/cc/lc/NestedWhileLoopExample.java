package com.kodnest.cc.lc;

public class NestedWhileLoopExample {

	public static void main(String[] args)
	{
	    int i=1;
	    while(i<=5)
	    {
	    	int j=1;
	    	while(j<=5)                        //nested while loop
	    	{
	    		System.out.print("Chinni ");
	    		j++;
	    	}
	    	System.out.println();
	    	i++;
	    }
	    
	    System.out.println("Bujji");
	    
	    	

	}

}
